def edad_pub_promedio(katalog: dict)->int:
    """ Retorna  el número entero más cercano el valor promedio de las edades a las que los autores del catálogo publicaron su obra principal

     Parámetros
     ----------
     katalog: dict
        El catálogo en el que se desea buscar
          
     Retorna
     ----------
     edad_prom: int
       Menor número entero igual o mayor que el valor promedio de las edades"""
    import math
    lista_autores=autores(katalog)
    edades=[]
    prom=0
    for n in lista_autores:
        edades.append(calcular_edad(katalog, f"{n}"))
    if edades != []:
         prom=sum(edades)/len(edades)
         validador=math.ceil(prom/0.5)*0.5
         if prom >= validador:
              return math.ceil(prom)
         else:
              return math.floor(prom)
    else:
         return "No se puede calcular el promedio. Por favor, verifique que se pueda calcular la edad de publicación y que el catálogo ingresado tenga los datos necesarios"

