#-------------FUNCIÓN QUE EXTRAE TODAS FECHAS DE CUALQUIER STRING-------------------------------------------------------------------------------

def extraer_fechas(texto: str)->list:
    """Extrae todas los años que aparecen en un texto.
    
    Parámetros
    ----------
    texto: str
        Texto del cual se extraerán las fechas

    Retorna
    -------
    fechas: list
        Lista de fechas extraídas del texto
    """
    texto=texto.replace("-", " ").replace(".", " ")
    fechas = []
    for palabra in texto.split():
        palabra = palabra.strip(",;:()[]{}")
        if palabra.endswith("."):
            palabra = palabra.replace(".", "")
        if palabra.isdigit() and len(palabra) == 4:
            fechas.append(palabra)

    return fechas

